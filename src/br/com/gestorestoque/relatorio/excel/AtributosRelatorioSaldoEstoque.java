package br.com.gestorestoque.relatorio.excel;

/**
 *
 * @author DG
 */
public class AtributosRelatorioSaldoEstoque {
    
    private String produto;
    private String lote;
    private String fornecedor;
    private String saldo;
    private String unidadeMedida;
    private String armazem;
    private String controladoPorLote;
    private String notaFiscal;
    private String preco;

    /**
     * @return the produto
     */
    public String getProduto() {
        return produto;
    }

    /**
     * @param produto the produto to set
     */
    public void setProduto(String produto) {
        this.produto = produto;
    }

    /**
     * @return the lote
     */
    public String getLote() {
        return lote;
    }

    /**
     * @param lote the lote to set
     */
    public void setLote(String lote) {
        this.lote = lote;
    }

    /**
     * @return the fornecedor
     */
    public String getFornecedor() {
        return fornecedor;
    }

    /**
     * @param fornecedor the fornecedor to set
     */
    public void setFornecedor(String fornecedor) {
        this.fornecedor = fornecedor;
    }

    /**
     * @return the saldo
     */
    public String getSaldo() {
        return saldo;
    }

    /**
     * @param saldo the saldo to set
     */
    public void setSaldo(String saldo) {
        this.saldo = saldo;
    }

    /**
     * @return the unidadeMedida
     */
    public String getUnidadeMedida() {
        return unidadeMedida;
    }

    /**
     * @param unidadeMedida the unidadeMedida to set
     */
    public void setUnidadeMedida(String unidadeMedida) {
        this.unidadeMedida = unidadeMedida;
    }

    /**
     * @return the armazem
     */
    public String getArmazem() {
        return armazem;
    }

    /**
     * @param armazem the armazem to set
     */
    public void setArmazem(String armazem) {
        this.armazem = armazem;
    }

    /**
     * @return the controladoPorLote
     */
    public String getControladoPorLote() {
        return controladoPorLote;
    }

    /**
     * @param controladoPorLote the controladoPorLote to set
     */
    public void setControladoPorLote(String controladoPorLote) {
        this.controladoPorLote = controladoPorLote;
    }

    /**
     * @return the notaFiscal
     */
    public String getNotaFiscal() {
        return notaFiscal;
    }

    /**
     * @param notaFiscal the notaFiscal to set
     */
    public void setNotaFiscal(String notaFiscal) {
        this.notaFiscal = notaFiscal;
    }

    /**
     * @return the preco
     */
    public String getPreco() {
        return preco;
    }

    /**
     * @param preco the preco to set
     */
    public void setPreco(String preco) {
        this.preco = preco;
    }
            
    
    
}
